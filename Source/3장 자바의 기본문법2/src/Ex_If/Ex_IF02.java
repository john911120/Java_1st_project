package Ex_If;

public class Ex_IF02 {
	/*
	 * 점수가 90점 이상이면 우등생 아니면 우등생이 아니라는 문자열을 출력하는 예제
	 * 3장 자바의 기초문법2 공부한 프로젝트가 날라가서 재 작성하고 저장 하는 단계입니다.
	 * if else문
	 */
	public static void main(String[] args) {
		int score = 30;
		if(score >= 90) {
			System.out.println("당신은 우등생입니다.");
		} else {
			System.out.println("당신은 우등생이 아닙니다.");
		}
	}

}
